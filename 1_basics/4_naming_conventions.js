/**
 * Naming Conventions
 *
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용할 수 있음
 * 2) 특수기호는 언더스코어와 달러를 사용할 수 있음
 * 3) 숫자로 이름을 시작할 수 없음
 *     1Name, 2Hello
 * 4) 키워드는 변수명으로 사용할 수 없음
 *     var const = 'var';
 *
 */

