/**
 * Variable 선언하기
 *
 * 1) var - 더 이상 사용하지 않음
 * 2) let - 변수
 * 3) const - 상수
 */

var nameVar = "팬텀";
console.log(nameVar);

var ageVar = 20;
console.log(ageVar);

let band = "Red Hot Chili Peppers";
console.log(band);

/**
 * let과 var로 선언하면
 * 값을 추후 변경할 수 있음
 * const는 변경 불가능함
 */

band = "AD/CD";
console.log(band);

const akatsuki = "紅月";
console.log(akatsuki);

/**
 * 선언과 할당
 *
 * 1) 변수를 선언하는 것
 * 2) 할당
 */

var name = "팬텀";
console.log(name);

let girlFriend;
console.log(girlFriend);
